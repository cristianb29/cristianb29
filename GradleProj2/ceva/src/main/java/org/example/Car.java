package org.example;

public class Car {
    private String marca;
    private String model;
    private String anFabr;
    private Boolean tipCombustibil;
    private Boolean inchiriat;
    private String nume;
    private String prenume;
    private String startInchiriere;
    private String endInchiriere;


    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getAnFabr() {
        return anFabr;
    }

    public void setAnFabr(String anFabr) {
        this.anFabr = anFabr;
    }

    public Boolean getTipCombustibil() {
        return tipCombustibil;
    }

    public void setTipCombustibil(Boolean tipCombustibil) {
        this.tipCombustibil = tipCombustibil;
    }

    public Boolean getInchiriat() {
        return inchiriat;
    }

    public void setInchiriat(Boolean inchiriat) {
        this.inchiriat = inchiriat;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String prenume) {
        this.prenume = prenume;
    }

    public String getStartInchiriere() {
        return startInchiriere;
    }

    public void setStartInchiriere(String startInchiriere) {
        this.startInchiriere = startInchiriere;
    }

    public String getEndInchiriere() {
        return endInchiriere;
    }

    public void setEndInchiriere(String endInchiriere) {
        this.endInchiriere = endInchiriere;
    }
}